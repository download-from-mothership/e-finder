def main():
    parser = argparse.ArgumentParser(description="DOJ Epstein Library Full Downloader")
    parser.add_argument("--manifest", default="./_pipeline_output/doj_full_manifest.json")
    parser.add_argument("--dest-dir", default="./doj_full_library")
    parser.add_argument("--section", help="Download only this section")
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--workers", type=int, default=2, help="Concurrent downloads (max 4)")
    parser.add_argument("--delay", type=float, default=0.5, help="Delay between downloads")
    parser.add_argument("--no-resume", action="store_true", help="Re-download all files")
    parser.add_argument("--max-retries", type=int, default=3)
    parser.add_argument("--report", action="store_true")
    args = parser.parse_args()

    args.workers = min(args.workers, 4)
    resume = not args.no_resume

    # Load manifest
    manifest_path = Path(args.manifest)
    if not manifest_path.exists():
        print(f"ERROR: Manifest not found at {manifest_path}")
        print("Run the browser extractor first, then save the manifest.")
        sys.exit(1)

    with open(manifest_path) as f:
        manifest = json.load(f)

    # Filter section if specified
    if args.section:
        if args.section not in manifest:
            print(f"ERROR: Section '{args.section}' not found. Available: {list(manifest.keys())}")
            sys.exit(1)
        manifest = {args.section: manifest[args.section]}

    # Stats
    total_files = sum(len(files) for files in manifest.values())
    total_sections = len(manifest)

    print(f"\n{'='*60}")
    print(f"  DOJ EPSTEIN LIBRARY DOWNLOADER")
    print(f"{'='*60}")
    print(f"  Manifest:    {manifest_path}")
    print(f"  Destination: {args.dest_dir}")
    print(f"  Sections:    {total_sections}")
    print(f"  Total files: {total_files:,}")
    print(f"  Workers:     {args.workers}")
    print(f"  Delay:       {args.delay}s")
    print(f"  Resume:      {resume}")
    print(f"{'='*60}")

    if args.dry_run:
        print("\n  DRY RUN — File counts by section:")
        for section, files in sorted(manifest.items()):
            exts = defaultdict(int)
            for f in files:
                ext = f.get("filename", "").rsplit(".", 1)[-1].lower() if "." in f.get("filename", "") else "unknown"
                exts[ext] += 1
            ext_str = ", ".join(f"{v} .{k}" for k, v in sorted(exts.items(), key=lambda x: -x[1]))
            print(f"    {section}: {len(files)} files ({ext_str})")

        # Estimate size (rough: avg 1MB per PDF)
        est_gb = total_files * 1.0 / 1024
        print(f"\n  Estimated total size: ~{est_gb:.0f} GB (assuming ~1MB avg per file)")
        print(f"  Estimated time: ~{total_files * args.delay / 3600:.1f} hours at {args.delay}s delay")
        return

    # Download
    dest = Path(args.dest_dir)
    dest.mkdir(parents=True, exist_ok=True)

    tracker = ProgressTracker(total_files, total_sections)

    # Process sections in order (smaller first for quick wins)
    section_order = sorted(manifest.keys(), key=lambda s: len(manifest[s]))

    for section_name in section_order:
        download_section(
            manifest[section_name], section_name, args.dest_dir,
            resume, args.max_retries, args.delay, args.workers, tracker
        )

    result = tracker.report()

    # Save report
    if args.report or True:  # Always save report
        report_path = Path(args.dest_dir) / "_download_report.json"
        with open(report_path, "w") as f:
            json.dump(result, f, indent=2, default=str)
        print(f"\n  Report saved to {report_path}")

    # Save failed files for retry
    if result["failed_files"]:
        retry_path = Path(args.dest_dir) / "_retry_manifest.json"
        retry_manifest = defaultdict(list)
        for f in result["failed_files"]:
            # Find original file info from manifest
            for sec_files in manifest.values():
                for fi in sec_files:
                    if fi.get("filename") == f["filename"]:
                        retry_manifest[f["section"]].append(fi)
                        break
        with open(retry_path, "w") as f_out:
            json.dump(dict(retry_manifest), f_out, indent=2)
        print(f"  Retry manifest saved to {retry_path}")
        print(f"  To retry: python3 {sys.argv[0]} --manifest {retry_path}")


if __name__ == "__main__":
    main()
