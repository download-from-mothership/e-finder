#!/usr/bin/env python3
"""
DOJ Epstein Library — Full Download Manager
=============================================
Downloads all files from the manifest generated by the browser-based extractor.
Supports resuming, parallel downloads, progress tracking, and integrity checks.

Usage:
  # Download everything (resumes automatically)
  python3 download_full_library.py

  # Download specific section only
  python3 download_full_library.py --section DataSet_01

  # Dry run - show what would be downloaded
  python3 download_full_library.py --dry-run

  # Set custom destination
  python3 download_full_library.py --dest-dir /path/to/storage

  # Adjust concurrency and delay
  python3 download_full_library.py --workers 3 --delay 0.5

Options:
  --manifest FILE    Path to manifest JSON (default: ./_pipeline_output/doj_full_manifest.json)
  --dest-dir DIR     Destination directory (default: ./doj_full_library)
  --section NAME     Only download this section
  --dry-run          Show stats without downloading
  --workers N        Concurrent download threads (default: 2, max 4)
  --delay N          Seconds between downloads per worker (default: 0.5)
  --resume           Skip files that already exist (default: True)
  --no-resume        Re-download all files even if they exist
  --max-retries N    Max retries per file (default: 3)
  --report           Generate download report after completion
"""

